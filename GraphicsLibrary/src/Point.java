
public class Point {
	public double xCoordinate;
	public double yCoordinate;
	
	/**
	 * Constructor to set the instance variables
	 * @param xCoordinate
	 * @param yCoordinate
	 */
	public Point(double xCoordinate, double yCoordinate) {
		this.xCoordinate = xCoordinate;
		this.yCoordinate = yCoordinate;
	}
}
